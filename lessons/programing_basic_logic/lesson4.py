# Tipos int e float
# int - Número inteiro
# O tipo int representa quaquer número
# Positivo ou negativo, int sem sinal é considerado positivo
print(11) # int
print(-11) # int
print(0)

# float - Número com ponto flutuante
# O tipo float representa quaquer número
# Positivo ou negativo com ponto flutuante
# float sem sinal é considerado positivo
print(1.1, 10.11) # float
print(0.0, -1.11) # float

# Afunção type mostra o tipo que o python inferiu ao valor
print(type("Cesar"))
print(type(15))
print(type(1.1),type(-1.1),type(0.0))